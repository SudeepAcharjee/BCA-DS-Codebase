# Integer variable
num_int = 10

# Floating-point variable
num_float = 3.14

# String variable
text_str = "Hello, Python!"

# Boolean variable
is_true = True
is_false = False

# List variable
list_var = [1, 2, 3, 4, 5]

# Tuple variable
tuple_var = (10, 20, 30, 40, 50)

# Dictionary variable
dict_var = {'name': 'John', 'age': 30, 'city': 'New York'}

# Set variable
set_var = {1, 2, 3, 4, 5}

# Complex variable
complex_var = 3 + 4j

# None variable
none_var = None

# Print all variables
print("Integer Variable:", num_int)
print("Float Variable:", num_float)
print("String Variable:", text_str)
print("Boolean Variables:", is_true, is_false)
print("List Variable:", list_var)
print("Tuple Variable:", tuple_var)
print("Dictionary Variable:", dict_var)
print("Set Variable:", set_var)
print("Complex Variable:", complex_var)
print("None Variable:", none_var)
